a = float(input())
b = float(input())
c = float(input())
campo_de_futebol = 4000
campos_a = a / campo_de_futebol
campos_b = b / campo_de_futebol
campos_c = c / campo_de_futebol
media = (campos_a + campos_b + campos_c) / 3
print(f"{campos_a:.2f}")
print(f"{campos_b:.2f}")
print(f"{campos_c:.2f}")
print(f"{media:.2f}")
